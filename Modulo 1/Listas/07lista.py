# index()
# Dada a lista letras = ["a", "b", "c", "d", "b"], use .index() para
# encontrar a posição da primeira ocorrência da letra "b". Mostre o índice retornado.


letras= ["a", "b", "c", "d", "b"]

posisao= letras.index("b") # armazenar a posiçao da letra
print(posisao) # peço para imprimir essa posiçao
print(letras.index("b")) # solicito a posiçao e imprimo